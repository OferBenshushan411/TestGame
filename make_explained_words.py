import unicodedata, re

NIKUD_RE = re.compile(r'[\u05B0-\u05C7\u05F0-\u05F4\uFB1D-\uFB4E]')

def strip_nikud(s):
    s = unicodedata.normalize('NFKD', s)
    return NIKUD_RE.sub('', s)

def auto_explain(word, level):
    """Generate a smart explanation based on word morphology."""
    stripped = strip_nikud(word).strip()
    w = stripped

    # Multi-word phrase
    if ' ' in w:
        return f"ביטוי: {word}"

    # Hif'il / Hitpa'el verbs (common in this dictionary)
    if w.startswith('ה') and len(w) >= 4:
        return f"פועל: {word}"
    if w.startswith('ה') and len(w) >= 3:
        return f"פועל: {word}"
    # Nouns with common prefixes
    if w.startswith('מ') and len(w) >= 4:
        return f"שם עצם: {word}"
    if (w.endswith('ה') or w.endswith('ות') or w.endswith('ית')) and len(w) >= 4:
        return f"שם עצם: {word}"
    if w.endswith('ן') or w.endswith('ם') or w.endswith('י'):
        return f"שם תואר/עצם: {word}"
    return f"מילה עברית: {word}"


def build_normalized_lookup(expl_dict):
    """Build a lookup table keyed by NFKD-normalized stripped word."""
    lookup = {}
    for key, val in expl_dict.items():
        norm = strip_nikud(key).strip()
        lookup[norm] = val
    return lookup


